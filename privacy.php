<h1>隐私政策</h1>
<p class="meta">最后更新：2026年6月26日</p>
<p>欢迎使用本论坛。我们非常重视您的隐私。本隐私政策旨在说明我们收集、使用和保护您的个人信息的方式。使用本论坛即表示您同意本隐私政策的条款。</p>
<h2>1. 我们收集的信息</h2>
<p>在您使用本论坛时，我们可能收集以下类型的信息：</p>
<ul>
<li><strong>注册信息</strong>：用户名、邮箱地址、密码（加密存储）。</li>
<li><strong>个人资料</strong>：头像、个人简介、背景图片等您主动填写的信息。</li>
<li><strong>使用数据</strong>：您发布的帖子、评论、私信、点赞、关注等互动行为。</li>
<li><strong>技术信息</strong>：IP地址、浏览器类型、设备信息、Cookie数据。</li>
<li><strong>第三方账号信息</strong>：如您通过 GitHub 或 Gitee 登录，我们将获取您的公开资料（昵称、头像、ID）。</li>
</ul>
<h2>2. 信息的使用</h2>
<p>我们收集的信息用于以下目的：</p>
<ul>
<li>提供、维护和改进论坛服务。</li>
<li>处理注册、登录和身份验证。</li>
<li>发送与账号相关的通知（如密码重置）。</li>
<li>防止欺诈、滥用和违反规定的行为。</li>
<li>分析使用趋势以改善用户体验。</li>
</ul>
<h2>3. 信息的存储与保护</h2>
<p>我们采取合理的技术措施保护您的个人信息免受未授权访问、修改或泄露。您的密码使用强哈希算法加密存储，我们不会以明文形式存储密码。</p>
<p>您的数据存储在位于中国境内的服务器上。我们将仅在为您提供服务所必需的期限内保留您的数据。</p>
<h2>4. 信息的共享</h2>
<p>我们不会将您的个人信息出售给第三方。在以下情况下，我们可能共享您的信息：</p>
<ul>
<li>应法律法规要求或政府机关合法要求。</li>
<li>为保护本论坛、用户或公众的权利、财产或安全。</li>
<li>与为我们提供服务的可信第三方合作（如服务器托管），但这些第三方必须遵守严格的保密义务。</li>
</ul>
<h2>5. Cookie 使用</h2>
<p>本论坛使用 Cookie 和类似技术来维持您的登录状态、记住您的偏好以及分析网站流量。您可以在浏览器设置中管理 Cookie 偏好。</p>
<h2>6. 第三方服务</h2>
<p>本论坛可能包含指向第三方网站的链接。我们不对这些网站的隐私实践负责。我们使用的第三方服务包括：</p>
<ul>
<li>GitHub OAuth（用于 GitHub 登录）</li>
<li>Gitee OAuth（用于 Gitee 登录）</li>
<li>不蒜子 / GetLoli 访问统计</li>
</ul>
<h2>7. 您的权利</h2>
<p>您有权：</p>
<ul>
<li>查看和编辑您的个人资料信息。</li>
<li>请求删除您的账号及相关数据。</li>
<li>拒绝 Cookie 追踪。</li>
<li>注销账号（可在个人资料页面操作）。</li>
</ul>
<h2>8. 未成年人保护</h2>
<p>我们不会故意收集未满14周岁未成年人的个人信息。如发现我们无意中收集了此类信息，请联系我们删除。</p>
<h2>9. 隐私政策的更新</h2>
<p>我们可能会不时更新本隐私政策。重大变更将通过论坛公告或弹窗通知您。</p>
<h2>10. 联系方式</h2>
<p>如您对本隐私政策有任何疑问或需要行使您的权利，请联系论坛管理员。</p>

